public class Loops_Demo {
    public static void main(String[] args) {
        // WHILE LOOP
        int i = 5;
        while(i > 0){
            System.out.println(i);
            i--;
        }

        System.out.println("========================");
        int a = 1;
        while(a <= 10){
            System.out.println(a);
            a++;
        }
    }
}
